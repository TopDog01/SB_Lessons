#include <iostream>
using namespace std;

int main() {

    setlocale(LC_ALL, "Russian");

    float price_per_100g; // цена за 100 грамм
    float weight;         // вес покупки
    float discount;       // размер скидки в процентах
    float total_cost;     // итоговая стоимость без скидки
    float final_cost;     // итоговая стоимость со скидкой

    // Запрашиваем у пользователя цену за 100 грамм
    cout << "Введите цену товара за 100 грамм (в рублях): ";
    cin >> price_per_100g;

    // Запрашиваем у пользователя вес покупки
    cout << "Введите вес покупки (в граммах): ";
    cin >> weight;

    // Запрашиваем у пользователя размер скидки в процентах
    cout << "Введите размер скидки (в процентах): ";
    cin >> discount;

    // Вычисляем итоговую стоимость без скидки
    total_cost = (price_per_100g / 100) * weight;

    // Вычисляем итоговую стоимость со скидкой
    final_cost = total_cost * (1 - discount / 100);

    // Выводим итоговую стоимость
    cout << "Итоговая стоимость покупки с учетом скидки: " << final_cost << " рублей." << endl;

    return 0;
}